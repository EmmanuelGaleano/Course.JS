// var
var helloworld = "Hello, JavaScript!";
console.log(helloworld);

helloworld = "Hello again, JavaScript!";
console.log(helloworld);

// let
let helloworld2 = "Hello, JavaScript2!";
console.log(helloworld2); 

helloworld2 = "Hello again, JavaScript2!";
console.log(helloworld2);

// const
const helloworld3 = "Hello, JavaScript3!";
console.log(helloworld3);

helloworld3 = "Hello again, JavaScript3!"; // This will cause an error
console.log(helloworld3);